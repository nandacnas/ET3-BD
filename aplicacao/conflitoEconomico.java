public class conflitoEconomico {
    
    private int cod_conflito;
    private String mat_prima;
    private String tipo;

    public int getCod_conflito() {
        return cod_conflito;
    }

    public void setCod_conflito(int cod_conflito) {
        this.cod_conflito = cod_conflito;
    }

    public String getMat_prima() {
        return mat_prima;
    }

    public void setMat_prima(String mat_prima) {
        this.mat_prima = mat_prima;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
}
